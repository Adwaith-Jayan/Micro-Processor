ASSUME CS:CODE, DS:DATA
DATA SEGMENT
	M1 DB 10,13, "HOW MANY? $"
	M2 DB 10,13, "ENTER NUMBER? $"
	M3 DB 10,13, "ENTER KEY: $"
	M4 DB 10,13, "FOUND $"
	M5 DB 10,13, "NOT FOUND $"
	ARRAY DB 10 DUP(?)
DATA ENDS

PRTMSG MACRO MESSAGE
	LEA DX,MESSAGE
	MOV AH,09
	INT 21H
ENDM

GETDCM MACRO
	MOV AH,01H
	INT 21H
	SUB AL,30H
ENDM

CODE SEGMENT
	START:
	MOV AX,DATA
	MOV DS,AX
	PRTMSG M1
	GETDCM
	MOV BL,AL
	MOV CL,BL
	MOV SI,OFFSET ARRAY
L1: PRTMSG M2
	GETDCM
	MOV [SI],AL
	INC SI
	DEC BL
	JNZ L1
	PRTMSG M3
	GETDCM
	
	MOV SI,OFFSET ARRAY
LP:	MOV BL,[SI]
	CMP AL,BL
	JZ FO
	INC SI
	DEC CL
	JNZ LP
	PRTMSG M5
	JMP DONE
FO:	PRTMSG M4
DONE: MOV AH,4CH
	INT 21H
CODE ENDS
END START
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
